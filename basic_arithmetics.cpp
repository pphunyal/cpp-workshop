#include <iostream>

using namespace std;

int main() 
{
    // Subtraction
    int sub = 8 - 4;
    cout << sub << endl;

    // Multiplication
    int mul = 8 * 4;
    cout << mul << endl;

    // Division
    int div = 8 / 5;
    cout << div << endl;
    
    // Remainder 
    int rem = 8 % 5;
    cout << rem << endl;
    
    return 0;
}